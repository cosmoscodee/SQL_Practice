SELECT JOB_ID FROM JOBS;

-- AC_ACCOUNT => SALARY + SALARY * 0.5
-- AC_MAG => SALARY + SALARY * 0.3
-- AC_VP => SALARY + SALARY * 0.2

SELECT EMPLOYEE_ID, SALARY, JOB_ID, 
	CASE JOB_ID 
			WHEN 'AC_ACCOUNT' THEN SALARY + SALARY * 0.5
			WHEN 'AC_MAG' THEN SALARY + SALARY * 0.3
			WHEN 'AC_VP' THEN SALARY + SALARY * 0.2 
	ELSE SALARY
			END AS "SARALY"
FROM EMPLOYEES;

--DECODE
SELECT EMPLOYEE_ID, SALARY, JOB_ID, 
	DECODE( JOB_ID,
			'AC_ACCOUNT', SALARY + SALARY * 0.5,
			'AC_MAG', SALARY + SALARY * 0.3,
			'AC_VP', SALARY + SALARY * 0.2, SALARY) AS "SARALY"
FROM EMPLOYEES;