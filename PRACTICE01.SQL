-- SQL 문제 1
SELECT CONCAT(FIRST_NAME, CONCAT(' ',LAST_NAME)) AS "이름", SALARY AS "연봉", PHONE_NUMBER AS "폰번호" 
FROM EMPLOYEES
ORDER BY HIRE_DATE ASC;

