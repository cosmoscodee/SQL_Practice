-- NULL TEST

-- NULL FUNCTION
-- NVL(NULL VALUE) 함수 , NVL2 

SELECT EMPLOYEE_id, SALARY, COMMISSION_PCT FROM EMPLOYEES;

SELECT EMPLOYEE_id, SALARY, SALARY *NVL(COMMISSION_PCT,0) AS "실수령", NVL(COMMISSION_PCT,0) AS "퍼센트" FROM EMPLOYEES;

SELECT NVL(NULL,0) FROM DUAL;

SELECT NVL2(NULL,10,20), NVL2(0,10,20) FROM DUAL; --NULL 이면 뒷값, 아니면 앞값

SELECT NVL(NULL,10) FROM DUAL; -- NULL 이면 10 이 출력댐

SELECT EMPLOYEE_ID, NVL(MANAGER_ID,0) FROM EMPLOYEES;
--SELECT EMPLOYEE_ID, NVL(MANAGER_ID,'없음') FROM EMPLOYEES; -- NVL() 함수 안 파라미터는 데이터타입이 같아야 한다.

-- NULLIF 결과가 같으면 NULL 다르면 앞에 값
SELECT NULLIF(10, 5+5) FROM DUAL;
SELECT NULLIF(10, 5+3) FROM DUAL;

-- COALESCE 
SELECT COALESCE(NULL, NULL, 1, 2, 3, 4) FROM DUAL;

